                            <h2 class="ltn__blog-title">Tips dan hal dasar Merawat bagian mesin motor Secara Mandiri</h2>
                            <div class="ltn__blog-meta">
                                <ul>
                                    <li class="ltn__blog-author">
                                        <a href="#"><img src="img/blog/29.jpg" alt="#">Oleh: Wahana Honda</a>
                                    </li>
                                    <li class="ltn__blog-date">
                                        <i class="far fa-calendar-alt"></i>27 September 2022
                                    </li>
                                   
                                </ul>
                            </div>
                            <p>Merawat motor penting  dilakukan secara rutin. Ada beberapa langkah mudah dan bisa dilakukan secara rutin  di rumah sambil sekaligus melakukan pengecekan.  Ada yang perlu dibawa ke bengkel untuk tindak  lanjut penanganan.</p>
                            <p>Langkah-langkah  awal ini perlu dilakukan untuk mencegah terjadinya kerusakan  parah pada kendaraan yang Anda gunakan.</p>
                            <img src="img\blog\28.jpg" alt="Image">
                            <h2>Wajib Pemanasan Tiap Pagi</h2>
                            <p>Ini langkah  perawatan paling mudah, tapi sekaligus juga  paling malas dilakukan. Padahal  memanasi mesin motor setiap pagi adalah langkah terbaik dan mudah,  sebagai  Langkah awal cara merawat motor yang palings ederhana</p>
                            <p>Pemanasan motor  perlu dilakukan supaya oli dalam mesin siap melumasi mesin sehingga performa dari mesin itu lebih maksimal</p>
                            <p>Dan sebaiknya memanasi mesin motor, ini dengan menggunakan kick starter supaya starter elektrik menjadi lebih awet.</p>
                            <p>Lakukan pemanasan motor paling tidak sepanjang 5 menit di setiap pagi saat, sebelum motor digunakan berkendara seharian.</p>
                            <h2>Cuci Rutin</h2>
                            <p>Ini penting:  lakukan pencucian motor minimal seminggu sekali, atau setiap kali sehabis kehujanan</p>
                            <p>Biasakan mencuci  dengan menggunakan air bersih dengan pembilasan untuk menghilangkan kotoran atau debu yang menempel.</p>
                            <p>Untuk pencucian penggunaan sabun, bisa dilakukan dalam kurun waktu sebulan sekali. Bisa juga ditambah dengan dengan menggunakan  bahan untuk mengkilapkan bodi motor. Ini merupakan langkah terbaik sebagai perawatan terhadap kondisi bodi motor agar tampilannya tetap menawan dan menarik.</p>
                            <h2>Ganti Oli Rutin</h2>
                            <p>Lakukan ganti oli secara rutin sesuai dengan jadwal penggantian. Umumnya, dilakukan setiap 1.000 Km. Sedangkan, untuk oli transmisi motor (oli gardan) pada umumnya dilakukan penggantian jika sudah mencapai 5.000 Km.</p>
                            <p>Jika lupa terhadap acuan jarak Km, bisa melakukan perawatan atau penggantian oli setiap menginjak 3 bulan sekali.</p>
                            <p>Disarankan gunakan oli asli  keluaran pabrik supaya sesuai dengan spesifikasi dari mesin yang ada.</p>
                            <h2>Perhatikan Posisi Bensin </h2>
                            <p>Untuk menjaga motor tetap dalam prima, jangan  biasakan motor kehabisan bensin atau bahan bakar. Dengan menjaga bensin  tetap dalam kondisi terisi, akan membuat mesin motor lebih awet.</p>
                            <p>Terlebih pada motor matic dengan teknologi injeksi. Karenanya usahakan motor matic sealu dalam posisi bensin yang aman. Hindari indikator pada speedometer  ada di posisi E.</p>
                            <p>Motor  yang terisi full bensin membuat perasaan aman, dari terjadinya kehabisan bensin di tengah jalan, apalagi posisi pom bensin  tak terjangkau  mata.</p>
                            <h2>V-Belt dan Roler</h2>
                            <p>Upayakan selalu mengecek kondisi V-Belt dan juga roler . V-Belt (khusus motor matic) terbuat dari karet,  yang rentan putus. Ini jelas berdampak pada mesin yang bisa  membuat motor berhenti dan tidak bisa dijalankan.</p>
                            <p>Karenanya penting melakukan cek kondisi V-Belt,  paling tidak dua minggu sekali. Jika kondisi karet v-belt terlihat aus, sebaiknya sesegera mungkin untuk dilakukan penggantian.</p>
                            <p>Gunakan v-belt orisinil sesuai pabrikan agar lebih sesuai dan pemakaiannya awet.</p>
                            <h2>Merawat Aki Rutin</h2>
                            <p>Aki juga perlu rutin dirawat. Mengingat, komponen ini sangat diperlukan untuk menunjang kelistrikan dalam sistem kerja motor.</p>
                            <p>Jika aki bermasalah, motor akan kehilangan power yang berdampak fatal, seperti cahaya lampu jadi kurang terang, starter  sulit dinyalakan dan kendala lainnya  yang berkaitan langsung dengan power pada aki.</p>
                            <p>Sangat disarankan melakukan penggantian aki setidaknya 8 bulan sampai 12 bulan sekali,  tergantung intensitas penggunaan motor itu sendiri.</p>
                            <h2>Jangan Gunakan Gas Mendadak</h2>
                            <p>Cara merawat motor yang harus perlu diketahui dan dilakukan adalah saat berkendara, jangan gunakan gas secara dadakan.</p>
                            <p>Gunakan gas dengan mode kecepatan yang bertahap. Jangan secara tiba-tiba, supaya mesin menjadi awet dan tidak mudah rusak.</p>
                            <p>Ketika motor selesai dipanaskan di waktu pagi hari, usahakan menarik gas secara pelan dan bertahap . Hal ini untuk mencegah mesin “kaget” saat mulai dikendarai.</p>
                            <h2>Service ke Bengkel Resmi</h2>
                            <p>Lakukan service  ke bengkel secara rutin. Service rutin meliputi penggantian oli mesin, pembersihan dan pengecekan karburator serta setting ulang motor sesuai standar yang ditentukan oleh pabrikan.</p>
                            <p>Di saat perawatan service  di bengkel, mintalah kepada mekanik saat melakukan pengecekan pada  kondisi aki, Van-Belt, Roler, kampas motor juga air-collant.</p>
                            <p>Lakukan service rutin seperti ini paling tidak 2 sampai 3 bulan sekali tergantung dari intensitas penggunaan motor.</p>
                            <p>Namun, jika dirasa motor yangd kendarai sudah terasa kurang nyaman tak perlu menunggu waktu 3 bulan, langsung bawa motor ke bengkel resmi untuk mendapatkan treatment perawatan dan perbaikan.</p>
                            <p>Itulah tips atau cara merawat motor yang benar dan tepat . beberapa diantaranya bisa dilakukan secara mandiri di rumah.</p>


