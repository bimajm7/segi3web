                            <h2 class="ltn__blog-title">11 Tips Agar Motor Awet Luar Dalam Paling Jitu, Ini Caranya!</h2>
                            <div class="ltn__blog-meta">
                                <ul>
                                    <li class="ltn__blog-author">
                                        <a href="#"><img src="img/blog/24.jpg" alt="#">Oleh AstraOtoshop</a>
                                    </li>
                                    <li class="ltn__blog-date">
