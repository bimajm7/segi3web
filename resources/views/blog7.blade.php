                            <h2 class="ltn__blog-title">Duh Gawat, Ini 6 Hal yang Bikin Motor Gampang Turun Mesin!</h2>
                            <div class="ltn__blog-meta">
                                <ul>
                                    <li class="ltn__blog-author">
                                        <a href="#"><img src="img/blog/27.jpg" alt="#">Oleh: medcom.id</a>
                                    </li>
                                    <li class="ltn__blog-date">
                                        <i class="far fa-calendar-alt"></i>20 November, 2023
                                    </li>
                                    
                                </ul>
                            </div>
                            <p>Jakarta: Turun mesin menjadi kata-kata yang tidak ingin Anda dengar ketika servis motor di bengkel. Sebab, bila sudah turun mesin artinya kamu perlu mengeluarkan uang hingga jutaan rupiah untuk mengembalikan mesin agar normal dan berfungsi kembali.</p>
                            <p>Turun mesin yang dimaksud di sini adalah servis besar yang membutuhkan banyak biaya karena masalah pada sepeda motor yang begitu serius. Metode turun mesin menjadi cara yang harus dilakukan agar masalah pada jantung mekanis teratasi. Istilah turun mesin sendiri digunakan karena mesin perlu dilepas lebih dulu dari rangkanya.</p>
                            <p>Suzuki kemudian menyebutkan sejumlah penyebab motor harus turun mesin meski jam terbangnya masih di bawah 60.000 km. Berikut berbagai penyebab yang dimaksud:</p>
                            <img src="img\blog\29.jpg" alt="Image">
                            <h2>Tidak Ganti Oli Tepat Waktu</h2>
                            <p>Periksa kunci kontak apakah dapat berfungsi dengan baik sebagai penyambung atau pemutus arus kelistrikan.</p>
                            <p>Fungsi oli bagi motor sangat vital, sebab menjadi elemen yang meminimalisir gesekan pada mesin serta mendinginkan suhu mesin agar tidak terlalu panas. Cara kerja oli adalah dengan mengisi berbagai celah yang terdapat pada roda gigi serta komponen mesin lain</p>
                            <p>Oli memastikan agar komponen mesin dapat bergerak lebih halus. Maka dari itu, kamu perlu memperhatikan jadwal penggantian oli agar tepat pada waktunya. Bila tidak tepat waktu, maka kamu menambah risiko gesekan antar komponen mesin semakin kasar.</p>
                            <p>Risiko tersebut akan berakhir merembet pada kondisi komponen mesin yang cepat aus. Selain itu, komponen seperti stang dan seher pun akan lebih berat lalu bisa bengkok bahkan patah.</p>
                            <p>Bila sudah seperti itu, kamu pun harus melakukan penggantian pada keseluruhan set mesin yang mana perlu dilakukan dengan turun mesin motor.</p>
                            <p>Jadi, lakukan ganti oli tepat waktu ketika motor sudah mencapai 4.000 km, atau empat bulan sekali. Namun, sesuaikan juga dengan intensitas pemakaian motor. Bila kamu berkendara di kondisi macet dan cuaca panas, sebaiknya ganti oli setiap dua bulan sekali atau ketika mencapai 2.500 km.</p>
                            <h2>Jarang Servis Rutin Motor</h2>
                            <p>Tidak hanya oli yang perlu diganti tepat waktu, kamu pun perlu servis motor keseluruhan secara rutin. Bila jaran servis, risikonya adalah turun mesin. Adapun langkah-langkah servis bagi kamu yang melakukannya di bengkel resmi harus diperhatikan.</p>
                            <p>Contohnya, untuk motor matik harus mengganti oli serta membersihkan filter udara setiap 2.000 hingga 3.0000 km, atau setiap 3 bulan. Lalu, bila kamu telah tiga kali melakukan ganti oli, lakukan penggantian oli gardan.</p>
                            <p>Ketika memasuki satu tahun, atau 12.000 km, seluruh komponen motor kamu akan dicek. Seperti ruang bakar, pengereman, injektor, hingga kelistrikan.</p>
                            <p>Tahap tersebut akan memberitahu kamu jika memang terdapat masalah pada mesin. Jadi, bagi yang jarang servis maka hasil pengecekan pun cenderung bermasalah.</p>
                            <h2>Suka Menerobos Banjir</h2>
                            <p>Penyebab turun mesin motor berikutnya adalah kebiasaan menerobos banjir, yang mana membuat air berisiko masuk ke mesin lalu tercampur dengan pelumas. Walaupun dampaknya tidak langsung terlihat, namun lambat laun kamu akan mendapati mesin berkarat dan baret.</p>
                            <h2>Bila sudah berkarat, motor akan mengalami mati mendadak ketika berkendara di tengah perjalanan. Masalah tersebut hanya dapat teratasi dengan metode turun mesin.</h2>
                            <p>Itulah mengapa, sebaiknya hindari menerobos banjir terlalu sering kecuali tidak ada alternatif jalan memutar lagi. Bila masih bisa putar balik dan melewati jalan alternatif, lakukan saja daripada di kemudian hari harus menanggung biaya yang jumlahnya tidak kecil.</p>
                            <h2>Tidak Rajin Mengganti Air Radiator</h2>
                            <p>Air radiator merupakan elemen yang dapat mendinginkan mesin motor saat komponennya bekerja. Bila malas mengganti air radiator, maka ketika habis mesin akan menjadi cepat panas karena elemen pendinginnya berkurang.</p>
                            <p>Mesin yang terlalu panas, atau overheating, maka mesin berisiko nge-jam. Kondisi tersebut ketika bagian di dalam mesin mengembang akibat suhu yang terlalu panas, lalu sirkulasi oli tidak berjalan, serta terlalu rapatnya ruang gerak piston.</p>
                            <p>Dampak berikutnya adalah motor jadi hilang tenaga karena pistonnya tidak dapat bergerak walaupun sudah digas. Bila dipaksakan, maka akan memicu kondisi yang harus turun mesin motor seperti stang seher patah hingga klep bengkok</p>
                            <h2>Modifikasi Mesin (Bore Up)</h2>
                            <p>Modifikasi mesin dengan tujuan meningkatkan kecepatan dan performa motor melalui peningkatan cc mesin adalah apa yang bisa kamu sebut sebagai bore up. Umumnya, bore up ini akan meningkatkan diameter piston menjadi lebih besar dari standar seharusnya.</p>
                            <p>Bore up juga membuat seluruh aspek mesin harus bekerja lebih keras dibandingkan standar dari pabrik. Hal tersebut akan memicu komponen mesin lebih cepat aus. Terutama apabila modifikasi mesin ini dilakukan bukan dengan teknisi profesional.</p>
                            <p>Dampaknya bila mesin sudah mencapai batas maksimal adalah komponen harus diperbaiki dengan turun mesin. Jadi, melakukan bore up tidak bisa sembarangan.</p>
                            <h2>Berkendara dengan Buruk</h2>
                            <p>Satu lagi penyebab turun mesin motor yang kerap disepelekan, yaitu berkendara tanpa memperhatikan prosedur pemakaiannya. Contoh umumnya mulai dari sering menggeber motor, lalu ugal-ugalan, hingga menarik gas dengan sembrono.</p>
                            <p>Hal tersebut akan memicu mesin lebih cepat aus. Bila sudah parah dan berlebihan, komponen pada mesin pun menjadi turun kualitasnya. Risiko perbaikan dengan turun mesin pun bukan tidak mungkin harus dilakukan.</p>
                            <p>Jadi, jangan meremehkan bagaimana berkendara sesuai prosedur dapat melindungi kamu dari kondisi turun mesin. Berkendaralah sesuai aturan, sehingga mesin lebih tahan lama dan berumur panjang.</p>

