                            <h2 class="ltn__blog-title">Apa yang Dilakukan Ketika Tune Up pada Mesin Motor?</h2>
                            <div class="ltn__blog-meta">
                                <ul>
                                    <li class="ltn__blog-author">
                                        <a href="#"><img src="img/blog/28.jpg" alt="#">By: Speedwork Autocare</a>
                                    </li>
                                    <li class="ltn__blog-date">
                                        <i class="far fa-calendar-alt"></i>26 Desember 2019
                                    </li>
                                    <li>
                                        <a href="#"><i class="far fa-comments"></i>35 Comments</a>
                                    </li>
                                </ul>
                            </div>
                            <p>Perawatan kendaraan merupakan hal wajib yang harus selalu rutin dilakukan. Perawatan rutin ini penting untuk menjaga kondisi kendaraan agar selalu tetap prima. Terkait performa kendaraan, tanpa Anda sadari semakin sering kendaraan digunakan maka performa kendaraan akan semakin menurun. Perawatan harus tetap rutin dilakukan walaupun tidak terjadi masalah pada kendaraan. Hal ini bertujuan untuk mencegah kerusakan pada sistem di kendaraan Anda.</p>
                            <p>Perawatan kendaraan biasanya dilakukan sesuai jadwal perawatan berkala atau ketika Anda merasa bahwa kendaraan Anda sudah tidak nyaman digunakan. Jadwal perawatan berkala menjelaskan persyaratan yang diperlukan dan item pengerjaan apa saja yang harus dilakukan dalam melakukan perawatan berkala. Anda bisa melihat jadwal dan item perawatan berkala pada buku servis yang Anda dapatkan dari dealer tempat Anda membeli kendaraan. Pada saat Anda akan melakukan servis motor, mungkin Anda sering menemukan istilah tune up yang ditawarkan di tiap bengkel-bengkel servis kendaraan. Akan tetapi, istilah tune up mungkin tidak akan Anda temui dalam buku servis kendaraan Anda.</p>
                            <img src="img\blog\27.jpg" alt="Image">
                            <h2>Jadi, apa yang dimaksud dengan Tune Up mesin motor pada saat servis?</h2>
                            <p>Tune up merupakan suatu tindakan atau pemeriksaan yang dilakukan pada kendaraan dengan tujuan untuk mengembalikan setting atau performa kendaraan ke kondisi semula. Hal ini dikarenakan perubahan yang terjadi terkait settingan, perhitungan dan performa kendaraan akibat kendaraan yang sering dioperasikan. Dampak dari perubahan kondisi ini menyebabkan tarikan mesin menjadi lebih berat, konsumsi bahan bakar menjadi lebih boros, dan suara mesin menjadi lebih kasar. Dalam melakukan tune up motor terdapat beberapa tindakan yang biasa dilakukan oleh teknisi bengkel untuk mengembalikan performa kendaraan ke kondisi awal. Berikut ini adalah beberapa aspek pemeriksaan yang dilakukan pada saat melakukan tune up:</p>
                            <h2>Memeriksa Filter Udara</h2>
                            <p>Filter udara mempengaruhi udara yang akan masuk ke intake manifold, kotoran yang terdapat pada filter akan mempengaruhi kuantitas udara yang masuk ke ruang bakar sehingga menyebabkan tenaga mesin menjadi tidak optimal. Oleh karena itu, filter udara harus selalu dibersihkan atau bahkan diganti.</p>
                            <h2>Pemeriksaan Busi</h2>
                            <p>Busi berperan penting terhadap pembakaran diruang bakar. Apabila busi sudah kotor atau berkerak akan menyebabkan percikan api menjadi tidak sempurna sehingga dapat mengganggu proses pembakaran. Pemeriksaan dan penggantian busi perlu dilakukan secara rutin</p>
                            <h2>Membersihkan Injector</h2>
                            <p>Pada motor injeksi, penyemprotan bahan bakar ke ruang bakar dilakukan oleh injector. Apabila injector kotor, maka akan menyebabkan jalur suplai bahan bakar tersumbat sehingga proses pembakaran tidak bisa berlangsung dengan optimal.</p>
                            <h2>Membersihkan Throttle Body</h2>
                            <p>Throttle body merupakan bagian gerbang utama untuk masuknya udara segar ke ruang bakar. Banyak sedikitnya kuantitas udara yang masuk dipengaruhi oleh besarnya bukaan katup throttle. Kondisi throttle yang kotor akan mempengaruhi bukaan katup sehingga menyebabkan putaran mesin menjadi tidak stabil.</p>
                            <h2>Penyetelan Celah Katup</h2>
                            <p>Pemeriksaan dan penyetelan celah katup atau biasa disebut stel klep perlu dilakukan untuk menjaga celah tetap dalam kondisi ideal serta mendapatkan kompresi mesin yang sempurna agar tercapai pembakaran yang optimal. Celah katup disetel agar celah tidak terlalu lebar maupun terlalu kecil.</p>


